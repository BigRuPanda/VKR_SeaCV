from ultralytics import YOLO

if __name__ == '__main__':
    # === Инициализация модели ===
    # Задаем базовое имя модели (yolo26n — нано-версия, оптимизированная по скорости/размеру)
    model_name = 'yolo26n'
    
    # Загружаем предобученные веса модели. 
    # Файл .pt содержит архитектуру сети и начальные веса, что позволяет использовать transfer learning
    model = YOLO(model_name + '.pt')

    # === Настройка и запуск процесса обучения ===
    results = model.train(
        data='seedronessees.yaml',      # Путь к конфигурационному файлу датасета (классы, пути к train/val/test)
        epochs=16,                      # Количество полных проходов по обучающей выборке
        imgsz=640,                      # Размер входного изображения (квадрат 640x640). Должен быть кратен 32
        batch=48,                       # Размер батча. Подбирается исходя из объема VRAM GPU
        device=0,                       # Индекс GPU для обучения (0 = первая видеокарта). Используйте 'cpu' для ЦП
        workers=4,                      # Кол-во потоков DataLoader для параллельной загрузки и аугментации данных
        project='seadrone_yolo',        # Директория проекта, куда будут сохраняться все артефакты обучения
        name=model_name + '_seadrone',  # Имя подпапки конкретного эксперимента внутри project
        exist_ok=True                   # Разрешить перезапись существующей папки эксперимента вместо создания новой с суффиксом
    )

    # Уведомление об успешном завершении пайплайна обучения
    print("Обучение завершено!")
